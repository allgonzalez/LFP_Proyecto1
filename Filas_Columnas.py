class FilasColumnas:

    def __init__(self, nombre, filas, columnas):
        self.nombre = nombre
        self.filas = filas
        self.columnas = columnas
    
    def getNombre(self):
        return self.nombre
    
    def getFilas(self):
        return self.filas
    
    def getColumnas(self):
        return self.filas